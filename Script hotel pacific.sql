
-- TABLA HOTEL
CREATE TABLE HOTEL (
    id_hotel        INTEGER      NOT NULL,
    nombre          CHAR(50),
    ciudad          CHAR(50),
    pais            CHAR(50),
    telefono        INTEGER,
    correo_electronico VARCHAR2(100),
    CONSTRAINT HOTEL_PK PRIMARY KEY (id_hotel)
);

-- TABLA CLIENTE:
CREATE TABLE CLIENTE (
    id_reserva      INTEGER      NOT NULL,
    nombre_completo VARCHAR2(100),
    genero          CHAR(10),
    fecha_nacimiento INTEGER,
    nacionalidad    CHAR(50),
    estado_civil    CHAR(20),
    telefono        INTEGER,
    correo_electronico VARCHAR2(100),
    direccion       VARCHAR2(150),
    comuna          CHAR(50),
    ciudad          CHAR(50),
    pais            CHAR(50),
    CLIENTE_ID      NUMBER NOT NULL,
    HOTEL_HOTEL_ID  NUMBER NOT NULL,
    CONSTRAINT CLIENTE_PK PRIMARY KEY (CLIENTE_ID),
    CONSTRAINT CLIENTE_HOTEL_FK FOREIGN KEY (HOTEL_HOTEL_ID)
    REFERENCES HOTEL(id_hotel)
);

-- TABLA RESERVA:
CREATE TABLE RESERVA (
    id_reserva      INTEGER      NOT NULL,
    fecha_reserva   INTEGER,
    fecha_de_llegada INTEGER,
    fecha_de_salida  INTEGER,
    cantidad_de_dias INTEGER,
    RESERVA_ID      NUMBER NOT NULL,
    CLIENTE_CLIENTE_ID NUMBER NOT NULL,
    CONSTRAINT RESERVA_PK PRIMARY KEY (RESERVA_ID),
    CONSTRAINT RESERVA_CLIENTE_FK FOREIGN KEY (CLIENTE_CLIENTE_ID)
    REFERENCES CLIENTE(CLIENTE_ID)
);

